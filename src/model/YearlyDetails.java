package model;

public class YearlyDetails {
	
	
  private String totalSubTotal;
  private String totalOrderTotal;
  private String totalVat;
  private String monthName;
	public String getTotalSubTotal() {
		return totalSubTotal;
	}
	public void setTotalSubTotal(String totalSubTotal) {
		this.totalSubTotal = totalSubTotal;
	}
	public String getTotalOrderTotal() {
		return totalOrderTotal;
	}
	public void setTotalOrderTotal(String totalOrderTotal) {
		this.totalOrderTotal = totalOrderTotal;
	}
	public String getTotalVat() {
		return totalVat;
	}
	public void setTotalVat(String totalVat) {
		this.totalVat = totalVat;
	}
	public String getMonthName() {
		return monthName;
	}
	public void setMonthName(String monthName) {
		this.monthName = monthName;
	}
	  

}
